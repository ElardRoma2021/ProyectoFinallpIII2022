    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        ClsInmuebleE objInmE=new  ClsInmuebleE();
        ClsInmuebleN objInmN=new ClsInmuebleN();
        objInmE.setUsuarioid(Integer.parseInt(txtCodigo.getText()));
        objInmE.setDireccioninmueble(txtDirecInmueble.getText());
        objInmE.setEstado("Nopublicado");
        objInmE.setNrohabitaciones(Integer.parseInt(txtNroHabitaciones.getText()));
        objInmE.setCiudad(txtCiudad.getText());
        objInmE.setDescripcion(txtDescripcion.getText());
        objInmE.setCategoria((String)CboCategoria.getSelectedItem());
        objInmE.setPrecio(Double.parseDouble(txtPrecio.getText()));
        File foto = new File(txtDireccion.getText());
        try{
            byte[] icono = new byte[(int) foto.length()];
            InputStream input = new FileInputStream(foto);
            input.read(icono);
            objInmE.setFoto(icono);
        }catch(Exception ex){
            objInmE.setFoto(null);
        }
        if (objInmN.MtdRegistrarInmueble(objInmE)== true) {
            JOptionPane.showMessageDialog(null, "Dato Guardados Correctamente...");
            MtdLimpiar();
        }else{
            JOptionPane.showMessageDialog(null, "Error de Datos...");
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

